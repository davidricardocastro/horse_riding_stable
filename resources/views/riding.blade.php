<h2>Ratsastus</h2> <!--horse riding-->

<p>Tarjoamme ratsastustunteja tiistaista lauantaihin, myös aamuisin ja aamupäivisin. Kaikki tuntimme keskittyvät kouluratsastukseen, muuta teemme myös tarvittaessa harjoituksia puomeilla. Ryhmien koko max. 4 ratsastajaa. Kaikilla hevosilla on käytössä koulusatula. Hevosten ehdoton maksimi työmäärä oppilaskäytössä on 2 tuntia päivässä. </p>

<h3>Ohjeita ratsastajalle</h3>
<p>Tahdomme säilyttää hevosemme positiivisina ja mielellään työskentelevinä. Huomioithan siis muutamia asioita:<p>
<ul>
    <li>Varaa riittävästi aikaa hevosen harjaamiseen ja varustamiseen ennen tuntia. Vältä kiirettä ja toimi rauhallisesti. Hevosille ei huudeta, eikä niitä kohdella kovakouraisesti.</li>
    <li>Satulavyön kiristäminen ei saa olla hevoselle epämiellyttävää, teethän sen siis asteittain muiden toimien lomassa.
    Kylmällä säällä lämmitä kuolaimet huolella ennen hevosen suuhun laittamista.</li>
    <li>Tunnin jälkeen hoida ratsusi huolella. Kesällä pyyhi hionneet kohdat sienellä. Talvella tai muuten kylmällä ilmalla, peittele hevonen villaviltillä joka löytyy sen karsinan edestä.</li>
</ul>
<p>Hoidamme ja varustamme myös mielellämme hevosen sinulle valmiiksi ennen tuntia ja pois tunnin jälkeen. Ilmoita vain ajoissa tästä tarpeesta!</p>
